package casualCodes;

public class Primerange {

	public static void main(String[] args) {

		int range = 100;

		for (int i = 1; i <= range; i++) {

			int count = 0;

			for (int j = 1; j <= i; j++) {

				if (j % i == 0)

					count++;
			}
		
			if (count==2)
				
				System.out.println();
			
			else
				System.out.println();
		

	
		}
		}

}
